									<p>	
										<h3>Tujuan</h3>
									</p>
									<br>	<br>	
									<p>	
										WANA CETTA bertujuan untuk mendedikasikan dan mengedukasi serta dapat menjadi wadah bagi Mahasiswa STMIK KOMPUTAMA untuk mencapai suatu tujuan yang bermanfaat bagi keluarga WNC, kampus dan masyarakat luas.Serta dapat menjadi organisasi yang dapat menampung aspirasi tentang lingkungan hidup di sekitar daerah lokal.
