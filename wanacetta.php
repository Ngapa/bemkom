										<h1>Sejarah Berdirinya UKM WANACETTA</h1>
									</p>
									<p>
										Nama MAPALA WANA CETTA STMIK KOMPUTAMA diambil dari bahasa sansekerta yaitu wana berarti hutan rimba dan cetta berarti berpengetahuan luas, jadi arti WNC  adalah hutan yang berpengetahuan luas dimana isi anggotanya dapat mengambil sebuah pengetahuan dari sebuah hutan, karena hutan merupakan  sumber hidup bagi para makhluk hidup dan ekosistem lainnya. WNC sendiri berdiri karena adanya diskusi panjang  dari Mahasiswa STMIK KOMPUTAMA  guna untuk melengkapi kegiatan di internal kampus sebagai Unit Kegiatan Mahasiswa (UKM). Di awal bulan November 2018 WNC terbentuk dengan nama PASAK yang berarti (Pencinta Alam Satu Adrenalin Komputama) dan dengan nama inilah WNC mengibarkan bendera di wilayah Majenang dan sekitarnya guna memperkenalkan nama STMIK KOMPUTAMA melalui UKM MAPALA ini.Dengan nama PASAK kami telah mendapatkan dukungan dari beberapa organisasi eksternal dan kami juga mendapatkan piala dari satu lomba lintas alam di SMA N 1 Cipari dengan kategori juara 3 Putra.Sepanjang 1 bulan perjalanan UKM ini ternyata nama PASAK tidak begitu populer oleh organisasi eksternal dan masyarakat luas akhirnya kami merumuskan untuk mengganti nama dengan logo yang sama dan dalam jangka waktu kurang lebih 1 minggu terbentuklah nama WANA CETTA. Kemudian kami merumuskan kembali tanggal, bulan dan tahun terbentuknya UKM ini. Pada saat itu semua keluarga WNC berkumpul tepat di bulan desember 2018 yang bertempat di Laksana Boga Majenang, dan akhirnya organisasi ini di resmikan oleh seluruh anggota pada 12 Desember 2018.Dan di awal tahun 2019 WNC mengajukan permohonan surat keputusan  terbentuknya UKM MAPALA WANA CETTA.Setelah UKM kami memakai nama WNC alhamdulillah nama STMIK KOMPUTAMA bisa dikenal di beberapa daerah.
									</p>
									<br>	<br>	
									<p>	
										<h3>Perkembangan</h3>
									</p>
									<p>	
										WANA CETTA  mengembangkan diri melalui pembelajaran yang mana mendatangkan pemateri sehingga anggota dapat lebih memahami teori dasar tentang kepencinta alaman, kami juga bekerja sama dengan beberapa organisasi ekternal seperti sispala maupun komunitas lainya guna dapat memupuk rasa kesolidaritasan yang tinggi sehingga mendapatkan chanel yang lebih banyak, serta keliling sekretariat  MAPALA2 di berbagai daerah yang tidak lain hanya untuk mempelajari ilmmu dasar MAPALA dan mencari format guna melangkah ke pendidikan dan latihan dasar  kepencinta alaman serta mengenalkan nama besar MAPALA kampus STMIK KOMPUTAMA MAJENANG, dan mengikuti pelatihan dasar serta ikut andil dalam kegiatan lingkungan hidup.
									</p>
